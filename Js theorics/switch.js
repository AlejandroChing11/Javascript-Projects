
var edadPepito = 20;

switch (edadPepito) {
    case 20:
        console.log("Pepito es mayor de edad");
        break;
    case 17:
        console.log("Pepito es menor de edad");
        break;
    case 30:
        console.log("Pepito es muy viejo");
        break;
    default:
        console.log("La edad de pepito es nula");
}
